console.log("Arithmetic operators in JS")

let a = 45
let b = 4

console.log("a + b =", a+b)
console.log("a - b =", a-b)
console.log("a / b =", a/b) // true division
console.log("a * b =", a*b)

console.log("a ** b =", a**b) // exponent
console.log("a%b =", a%b) // remainder

console.log("a++ =", a++)
console.log(a)

console.log("a-- =", a--)
console.log(a)

console.log("++a =", ++a)
console.log(a)

console.log("--a =", --a)
console.log(a)



